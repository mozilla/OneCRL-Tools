func BenchmarkCertdata(b *testing.B) {
	// Stop the benchmark so that network IO doesn't poison the test.
	b.StopTimer()
	r, err := http.Get(URL)
	if err != nil || r.StatusCode != http.StatusOK {
		b.Logf("Problem fetching certdata.txt data from %s, status code %d, error %s\n", URL, r.StatusCode, err)
		b.FailNow()
	}
	c, err := ioutil.ReadAll(r.Body)
	r.Body.Close()
	if err != nil {
		b.Logf("Problem reading from the response body. Got %v, error %v", c, err)
		b.FailNow()
	}
	b.StartTimer()
	for i := 0; i < b.N; i++ {
		ParseToNormalizedForm(bytes.NewReader(c))
	}
}
